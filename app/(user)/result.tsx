import { MaterialCommunityIcons } from '@expo/vector-icons';
import AsyncStorage from '@react-native-async-storage/async-storage';
import Constants from 'expo-constants';
import * as Print from 'expo-print';
import { useLocalSearchParams, useRouter } from 'expo-router';
import * as Sharing from 'expo-sharing';
import React, { useEffect, useState } from 'react';
import {
  ActivityIndicator,
  Alert, Dimensions,
  Image,
  Linking,
  Platform,
  ScrollView,
  StatusBar,
  StyleSheet,
  Text,
  TouchableOpacity,
  View
} from 'react-native';
import { BASE_URL } from '../config';

const { width } = Dimensions.get('window');

// 1. Disease code to full name mapping
const diseaseNames: Record<string, string> = {
  'akiec': 'Actinic Keratoses',
  'bcc': 'Basal Cell Carcinoma',
  'bkl': 'Benign Keratosis',
  'mel': 'Melanoma',
  'nv': 'Melanocytic Nevi',
  'vasc': 'Vascular Lesions'
};

// 2. Doctor type definition
type Doctor = {
  name: string;
  specialization: string;
  hospital: string;
  district: string;
  contact: string;
};

export default function ResultScreen() {
  const router = useRouter();
  const params = useLocalSearchParams();
  
  const [currentDate, setCurrentDate] = useState('');
  
  // State Variables (Doctors Section)
  const [doctors, setDoctors] = useState<Doctor[]>([]);
  const [userDistrict, setUserDistrict] = useState<string>('Loading...');
  const [loadingDoctors, setLoadingDoctors] = useState(true);

  // 2. Data extraction from route params
  const imageUri = params.imageUri as string;
  const predictionCode = (params.prediction as string) || 'unknown';
  const confidenceLevel = (params.confidence as string) || '0%';
  
  // 3. Confidence level parsing
  const confidenceNumber = parseFloat(confidenceLevel.replace('%', '')) || 0;
  const formattedConfidence = Math.round(confidenceNumber) + '%';
  const fullDiseaseName = diseaseNames[predictionCode] || 'Unknown Condition';

  // Risk Calculations
  const isHighRisk = predictionCode === 'mel' || predictionCode === 'bcc';
  const riskText = isHighRisk ? 'High Risk' : 'Medium Risk';
  const riskColor = isHighRisk ? '#EF4444' : '#EF6C00'; 
  const riskBgColor = isHighRisk ? '#FEE2E2' : '#FFF3E0';

  
  useEffect(() => {
    const now = new Date();
    const dateString = now.toLocaleString();
    setCurrentDate(dateString);

    const processInitialData = async () => {
      try {
        const userEmail = await AsyncStorage.getItem('userEmail'); 
        if (!userEmail) return; 

        //  1. Auto Save History 
        const newRecord = {
          id: Date.now().toString(),
          user_email: userEmail, 
          condition: fullDiseaseName,
          risk: isHighRisk ? 'High' : (predictionCode === 'nv' ? 'Low' : 'Medium'),
          confidence: formattedConfidence,
          date: dateString, 
          timestamp: Date.now()
        };
        
        fetch(`${BASE_URL}/history/save`, {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify(newRecord)
        }).catch(err => console.error("Auto-save failed:", err));

        //  2. Fetch User Profile to get District 
        const profileRes = await fetch(`${BASE_URL}/user/${userEmail}`);
        const profileData = await profileRes.json();
        
        const district = (profileData.district || 'Colombo').trim(); 
        
        console.log("Fetching doctors for district:", district); 
        setUserDistrict(district);

        //  3. Fetch Doctors by District 
        const docRes = await fetch(`${BASE_URL}/doctors/${district}`);
        const docData = await docRes.json();
        
        if (docData.success && docData.doctors) {
          setDoctors(docData.doctors);
        }

      } catch (error) {
        console.error("Error fetching data:", error);
      } finally {
        setLoadingDoctors(false);
      }
    };

    processInitialData();
  }, []); 

  // Call Doctor Function
  const makePhoneCall = (phoneNumber: string) => {
    Linking.openURL(`tel:${phoneNumber}`);
  };

  // PDF generation and sharing function
  const generateAndSharePDF = async () => {
    try {
      const htmlContent = `
        <html>
          <body style="font-family: Arial, sans-serif; padding: 40px; color: #333;">
            <div style="text-align: center; border-bottom: 2px solid #1976D2; padding-bottom: 20px; margin-bottom: 30px;">
              <h1 style="color: #1976D2; margin: 0;">Skin Disease Analysis Report</h1>
              <p style="color: #666; margin-top: 5px;">Generated by MYSKINAPP</p>
            </div>
            
            <table style="width: 100%; border-collapse: collapse; margin-bottom: 30px;">
              <tr>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee;"><strong>Date & Time:</strong></td>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee; text-align: right;">${currentDate}</td>
              </tr>
              <tr>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee;"><strong>Predicted Condition:</strong></td>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee; text-align: right; font-size: 18px; font-weight: bold;">${fullDiseaseName}</td>
              </tr>
              <tr>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee;"><strong>Risk Level:</strong></td>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee; text-align: right; color: ${riskColor}; font-weight: bold;">${riskText}</td>
              </tr>
              <tr>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee;"><strong>Confidence Score:</strong></td>
                <td style="padding: 10px 0; border-bottom: 1px solid #eee; text-align: right; font-weight: bold; color: #1976D2;">${formattedConfidence}</td>
              </tr>
            </table>

            <div style="margin-top: 30px; padding: 15px; border: 1px solid #EF6C00; border-radius: 8px; background-color: #FFF3E0;">
              <p style="margin: 0; color: #EF6C00; font-size: 14px; text-align: center;">
                <strong>Important Disclaimer:</strong> This screening tool does not replace professional medical diagnosis.
              </p>
            </div>
          </body>
        </html>
      `;

      const { uri } = await Print.printToFileAsync({
        html: htmlContent,
        base64: false
      });

      await Sharing.shareAsync(uri, {
        mimeType: 'application/pdf',
        dialogTitle: 'Save or Share Analysis Report',
        UTI: 'com.adobe.pdf'
      });

    } catch (error) {
      console.error("Error generating PDF:", error);
      Alert.alert("Error", "Could not generate the report. Please try again.");
    }
  };

  return (
    <View style={styles.container}>
      <StatusBar barStyle="light-content" backgroundColor="#1976D2" />

      {/* 1. Header Section */}
      <View style={styles.header}>
        <TouchableOpacity onPress={() => router.back()}>
          <MaterialCommunityIcons name="arrow-left" size={26} color="white" />
        </TouchableOpacity>
        <Text style={styles.headerTitle}>Analysis Result</Text>
        <View style={{ width: 26 }} />
      </View>

      <ScrollView contentContainerStyle={styles.scrollContent} showsVerticalScrollIndicator={false}>
        
        {/* 2. Processing Time & Date */}
        <View style={styles.timeInfoRow}>
          <Text style={styles.processingTime}>Processing time: <Text style={{color: '#1976D2', fontWeight: 'bold'}}>2.4s</Text></Text>
          <Text style={styles.dateTime}>{currentDate}</Text>
        </View>

        {/* 3. Analyzed Image Section */}
        <Text style={styles.sectionLabel}>Analyzed Image</Text>
        <View style={styles.imageContainer}>
          {imageUri ? (
             <Image source={{ uri: imageUri }} style={styles.resultImage} />
          ) : (
             <View style={styles.imagePlaceholder}>
                <MaterialCommunityIcons name="image-broken-variant" size={50} color="#CBD5E1" />
                <Text style={{color: '#94A3B8'}}>Image not found</Text>
             </View>
          )}
        </View>

        {/* 4. Predicted Condition Card */}
        <View style={styles.card}>
          <View style={styles.cardHeaderRow}>
            <Text style={styles.cardLabel}>Predicted Condition</Text>
            <View style={[styles.riskBadge, { backgroundColor: riskBgColor }]}>
              <Text style={[styles.riskBadgeText, { color: riskColor }]}>{riskText}</Text>
            </View>
          </View>
          <Text style={[styles.conditionTitle, isHighRisk && { color: '#EF4444' }]}>
            {fullDiseaseName}
          </Text>
        </View>

        {/* 5. AI Confidence Score Card */}
        <View style={styles.card}>
          <View style={styles.scoreRow}>
            <Text style={styles.cardLabel}>AI Confidence Score</Text>
            <Text style={styles.scoreValue}>{formattedConfidence}</Text>
          </View>
          <View style={styles.progressBarBg}>
            <View style={[styles.progressBarFill, { width: `${confidenceNumber}%` }]} />
          </View>
          <Text style={styles.scoreSub}>Model prediction confidence level</Text>
        </View>

        {/* 6. Accuracy Score Card */}
        <View style={styles.card}>
          <View style={styles.accuracyRow}>
             <View style={styles.circularProgress}>
                <Text style={styles.circularText}>{formattedConfidence}</Text>
             </View>
             <View style={styles.accuracyTextContent}>
                <Text style={styles.accuracyTitle}>Accuracy Score</Text>
                <Text style={styles.accuracyDesc}>Estimated probability based on visual lesion characteristics</Text>
             </View>
          </View>
        </View>

        {/* 7.Doctors Recommendations Section */}
        <View style={styles.specialistSection}>
          <View style={styles.infoTitleRow}>
            <MaterialCommunityIcons name="stethoscope" size={24} color="#1976D2" />
            <Text style={styles.infoHeading}>Specialists in {userDistrict}</Text>
          </View>

          {loadingDoctors ? (
            <ActivityIndicator size="small" color="#1976D2" style={{ marginTop: 15 }} />
          ) : doctors.length > 0 ? (
            <ScrollView horizontal showsHorizontalScrollIndicator={false} style={styles.doctorScroll}>
              {doctors.map((doc, index) => (
                <View key={index} style={styles.doctorCard}>
                  <View style={styles.docAvatar}>
                    <MaterialCommunityIcons name="doctor" size={32} color="#1976D2" />
                  </View>
                  <Text style={styles.docName} numberOfLines={1}>{doc.name}</Text>
                  <Text style={styles.docSpec}>{doc.specialization}</Text>
                  <Text style={styles.docHosp} numberOfLines={1}>{doc.hospital}</Text>
                  
                  <TouchableOpacity style={styles.callBtn} onPress={() => makePhoneCall(doc.contact)}>
                    <MaterialCommunityIcons name="phone" size={16} color="white" />
                    <Text style={styles.callBtnText}>Call Now</Text>
                  </TouchableOpacity>
                </View>
              ))}
            </ScrollView>
          ) : (
            <View style={styles.noDocCard}>
              <Text style={{color: '#64748B', textAlign: 'center'}}>
                We couldn't find specialists in {userDistrict}. Please visit your nearest general hospital.
              </Text>
            </View>
          )}
        </View>

        {/* 8. Important Disclaimer Card */}
        <View style={[styles.card, { borderColor: '#E2E8F0', marginTop: 10 }]}>
          <View style={styles.infoTitleRow}>
            <MaterialCommunityIcons name="alert-circle-outline" size={20} color="#EF6C00" />
            <Text style={styles.infoHeading}>Important Disclaimer</Text>
          </View>
          <Text style={styles.infoContent}>
            This screening tool does <Text style={{fontWeight: 'bold'}}>NOT replace professional medical diagnosis</Text>. Always consult a dermatologist or healthcare professional for proper evaluation and treatment.
          </Text>
        </View>

        {/* 9. Action Buttons */}
        <View style={styles.buttonGroup}>
          <TouchableOpacity style={styles.reportBtn} onPress={generateAndSharePDF}>
            <MaterialCommunityIcons name="file-pdf-box" size={24} color="#1976D2" />
            <Text style={styles.reportBtnText}>Download / Share Report</Text>
          </TouchableOpacity>

          <TouchableOpacity style={styles.secondaryBtn} onPress={() => router.push('/(user)/(tabs)')}>
            <MaterialCommunityIcons name="refresh" size={20} color="#1976D2" />
            <Text style={styles.secondaryBtnText}>Analyze Another</Text>
          </TouchableOpacity>
        </View>

      </ScrollView>
    </View>
  );
}

